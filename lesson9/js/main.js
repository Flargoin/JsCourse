const fruit = "Some fruit";
console.log(fruit.indexOf("fruit")); /* Показывает с какого индекса(позиции) начинаеться искомое значение, если результат -1 , то это значит что ничего не найдено (это поиск подстроки) */

const logg = "Hello World";
console.log(logg.slice(6,11)); /* Метод вырезания из строки, 2 аргумента (начальная позиция, конечная позиция(не включая)), либо указываеться 1 аргумент(начальная позиция) и будет вырезаться с начальной до самого конца строки */
console.log(logg.substring(6,11)); /* Тоже самое что и выше, только не поддерживает отрицательные значения */
console.log(logg.substr(6,11)); /* Тоже самое что и выше, только вместо конечной позиции вводиться длина вырезания строки */



