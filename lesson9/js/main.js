/* МЕТОДЫ ДЛЯ СТРОК - пишутся как и функции .toUpperCase() */

console.log(str.toUpperCase()); /* Никак не влияет на значение переменной str, с помощью метода создаёться новое значение */
console.log(str.toLowerCase()); 
